
public class Swapping {

	public static void main(String[] args)
	{
		// Swapping to Variables 
		
		String x = " Fakhar " ;
		String y = " Butt " ;
		String temp = null ;
		
		// Before Swapping 
		System.out.println(x + y );
		
		temp = x ;
		x = y ;
		y = temp ;
		
		// After Swapping 
		
		System.out.print(x + y);

	}

}
